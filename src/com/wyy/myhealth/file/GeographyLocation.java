package com.wyy.myhealth.file;

/**
 * 地理位置信息类
 * 
 * @author lyl
 * 
 */
public class GeographyLocation {
	// 纬度
	private double lat = 0;
	// 经度
	private double lon = 0;

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}

	public double getLon() {
		return lon;
	}

	public void setLon(double lon) {
		this.lon = lon;
	}

}
